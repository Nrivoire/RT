#include "../includes/libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t	i;

	i = -1;
	while (++i < n)
		*(unsigned char*)dst++ = *(unsigned const char*)src++;
	return (dst - i);
}
