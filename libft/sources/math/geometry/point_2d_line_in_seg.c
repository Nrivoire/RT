#include "libft.h"

int	point_2d_line_in_seg(t_real p, t_2d_seg s)
{
	return (((p.x >= s.a.x && p.x <= s.b.x) 		\
				|| (p.x >= s.b.x && p.x <= s.a.x)) 	\
			&& ((p.y >= s.a.y && p.y <= s.b.y) 		\
				|| (p.y >= s.b.y && p.y <= s.a.y)));
}
